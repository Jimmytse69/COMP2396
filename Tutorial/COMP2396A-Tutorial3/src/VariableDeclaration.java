
public class VariableDeclaration {

	public static void main(String[] args) {
		long l = 3456789l;
		float f = 32.5f;
		double d = 3456.98d;

		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		
		System.out.println();
		System.out.println();

		long l2 = 3456789L;
		float f2 = 32.5F;
		double d2 = 3456.98D;

		System.out.println(l2);
		System.out.println(f2);
		System.out.println(d2);

	}

}
